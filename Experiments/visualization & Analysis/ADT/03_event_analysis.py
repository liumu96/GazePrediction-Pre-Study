#!/usr/bin/env python
"""Analyze SparseGaze angular error by GT scene-gaze event label.

This script builds on ``02_mae_visualization.py``. It groups evaluated
SparseGaze frames by the scene-event labels generated by the ADT pipeline,
then writes event-level statistics and figures.

python "Experiments/visualization & Analysis/ADT/03_event_analysis.py" \
  --start-frame 0 --end-frame 120
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path
from typing import Any

os.environ.setdefault("MPLCONFIGDIR", "/tmp/matplotlib")

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

def find_repo_root() -> Path:
    path = Path(__file__).resolve()
    for parent in [path.parent, *path.parents]:
        if (parent / "src").exists() and (parent / "Experiments").exists():
            return parent
    return Path(__file__).resolve().parents[3]


REPO_ROOT = find_repo_root()
sys.path.insert(0, str(REPO_ROOT))
sys.path.insert(0, str(REPO_ROOT / "src"))

from analysis.prediction_results import load_prediction_frame  # noqa: E402

SEQUENCE = "Apartment_release_decoration_skeleton_seq133_M1292"
DEFAULT_REPORTS_DIR = Path(
    os.environ.get("REPORTS_DIR", "/mnt/d/SparseGaze/ADT-Gaze-structured")
)
DEFAULT_PREDICTION_NPZ = Path(
    "/home/liumu/Github_Projects/SparseGaze/outputs/eval/adt/sparsegaze/test/rollout/"
    f"sequence_predictions/{SEQUENCE}/hz6_phase0.npz"
)
DEFAULT_OUTPUT_ROOT = REPO_ROOT / "outputs" / "figures" / "event_analysis"
EVENT_ORDER = ["fixation", "transition", "invalid", "unknown"]
EVENT_COLORS = {
    "fixation": "#2ca25f",
    "transition": "#fdae61",
    "invalid": "#d73027",
    "unknown": "#bdbdbd",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--prediction-npz", type=Path, default=DEFAULT_PREDICTION_NPZ)
    parser.add_argument("--reports-dir", type=Path, default=DEFAULT_REPORTS_DIR)
    parser.add_argument("--start-frame", type=int, default=0)
    parser.add_argument("--end-frame", type=int, default=None)
    parser.add_argument("--output-root", type=Path, default=DEFAULT_OUTPUT_ROOT)
    parser.add_argument("--run-name", default=None)
    parser.add_argument(
        "--include-anchors",
        action="store_true",
        help="Include anchor frames. By default only eval_mask frames are analyzed.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    frame = load_prediction_frame(args.prediction_npz, reports_dir=args.reports_dir)
    window = select_window(frame, args.start_frame, args.end_frame)
    analyzed = window.copy() if args.include_anchors else window[window["eval_mask"]].copy()
    if analyzed.empty:
        raise ValueError("No frames selected for event analysis.")
    if "scene_event_label" not in analyzed.columns:
        raise ValueError("Scene event labels are missing; check --reports-dir.")

    sequence = str(frame["sequence"].iloc[0])
    run_name = args.run_name or default_run_name(
        args.prediction_npz,
        args.start_frame,
        args.end_frame,
    )
    output_dir = args.output_root / sequence / run_name
    output_dir.mkdir(parents=True, exist_ok=True)

    event_summary = summarize_by_event(analyzed)
    event_summary.to_csv(output_dir / "event_error_summary.csv", index=False)
    analyzed.to_csv(output_dir / "event_labeled_frame_errors.csv", index=False)

    contrast = summarize_event_contrast(event_summary)
    write_json(output_dir / "summary.json", build_summary(args, frame, window, analyzed, contrast))

    fig = make_event_distribution_figure(analyzed, event_summary)
    fig.savefig(output_dir / "event_error_distribution.png", dpi=180)
    plt.close(fig)

    fig = make_event_bar_figure(event_summary)
    fig.savefig(output_dir / "event_error_mean_p90.png", dpi=180)
    plt.close(fig)

    print(event_summary.round(4).to_string(index=False))
    if contrast:
        print(
            "transition_minus_fixation_mae_deg: "
            f"{contrast['transition_minus_fixation_mae_deg']:.4f}"
        )
    print(f"output_dir: {output_dir}")


def select_window(frame: pd.DataFrame, start_frame: int, end_frame: int | None) -> pd.DataFrame:
    if start_frame < 0:
        raise ValueError("start_frame must be non-negative")
    if end_frame is not None and end_frame <= start_frame:
        raise ValueError("end_frame must be greater than start_frame")
    mask = frame["frame_index"] >= start_frame
    if end_frame is not None:
        mask &= frame["frame_index"] < end_frame
    selected = frame.loc[mask].copy()
    if selected.empty:
        raise ValueError("No frames selected; check --start-frame/--end-frame.")
    return selected


def summarize_by_event(frame: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for label in ordered_labels(frame["scene_event_label"].astype(str)):
        group = frame[frame["scene_event_label"].astype(str) == label]
        rows.append(
            {
                "scene_event_label": label,
                "n": int(len(group)),
                "mae_deg": float(group["angular_error_deg"].mean()),
                "median_deg": float(group["angular_error_deg"].median()),
                "p75_deg": float(np.percentile(group["angular_error_deg"], 75)),
                "p90_deg": float(np.percentile(group["angular_error_deg"], 90)),
                "max_deg": float(group["angular_error_deg"].max()),
                "mean_abs_yaw_error_deg": float(group["yaw_error_deg"].abs().mean()),
                "mean_abs_pitch_error_deg": float(group["pitch_error_deg"].abs().mean()),
            }
        )
    return pd.DataFrame(rows)


def ordered_labels(values: pd.Series) -> list[str]:
    present = set(values)
    ordered = [label for label in EVENT_ORDER if label in present]
    ordered.extend(sorted(present - set(ordered)))
    return ordered


def summarize_event_contrast(summary: pd.DataFrame) -> dict[str, float]:
    by_label = summary.set_index("scene_event_label")
    if "fixation" not in by_label.index or "transition" not in by_label.index:
        return {}
    fixation = by_label.loc["fixation"]
    transition = by_label.loc["transition"]
    return {
        "fixation_mae_deg": float(fixation["mae_deg"]),
        "transition_mae_deg": float(transition["mae_deg"]),
        "transition_minus_fixation_mae_deg": float(
            transition["mae_deg"] - fixation["mae_deg"]
        ),
        "fixation_n": int(fixation["n"]),
        "transition_n": int(transition["n"]),
    }


def make_event_distribution_figure(frame: pd.DataFrame, summary: pd.DataFrame) -> plt.Figure:
    labels = list(summary["scene_event_label"])
    data = [
        frame.loc[frame["scene_event_label"].astype(str) == label, "angular_error_deg"].to_numpy()
        for label in labels
    ]
    colors = [EVENT_COLORS.get(label, "#8c8c8c") for label in labels]
    fig, ax = plt.subplots(figsize=(10, 5.8))
    box = ax.boxplot(
        data,
        tick_labels=labels,
        patch_artist=True,
        showfliers=False,
        medianprops={"color": "black", "linewidth": 1.4},
    )
    for patch, color in zip(box["boxes"], colors, strict=True):
        patch.set_facecolor(color)
        patch.set_alpha(0.45)
    jittered_scatter(ax, data, colors)
    ax.set_ylabel("Angular error [deg]")
    ax.set_title("SparseGaze error distribution by GT scene-gaze event")
    ax.grid(axis="y", alpha=0.25)
    fig.tight_layout()
    return fig


def jittered_scatter(axis: plt.Axes, data: list[np.ndarray], colors: list[str]) -> None:
    rng = np.random.default_rng(7)
    for index, (values, color) in enumerate(zip(data, colors, strict=True), start=1):
        if len(values) == 0:
            continue
        if len(values) > 600:
            values = rng.choice(values, size=600, replace=False)
        x = index + rng.uniform(-0.16, 0.16, size=len(values))
        axis.scatter(x, values, s=7, color=color, alpha=0.18, linewidths=0)


def make_event_bar_figure(summary: pd.DataFrame) -> plt.Figure:
    labels = list(summary["scene_event_label"])
    x = np.arange(len(labels), dtype=float)
    colors = [EVENT_COLORS.get(label, "#8c8c8c") for label in labels]
    fig, ax = plt.subplots(figsize=(10, 5.5))
    ax.bar(x - 0.18, summary["mae_deg"], width=0.36, color=colors, alpha=0.78, label="Mean")
    ax.bar(
        x + 0.18,
        summary["p90_deg"],
        width=0.36,
        color=colors,
        alpha=0.38,
        hatch="//",
        label="P90",
    )
    for i, row in summary.iterrows():
        ax.text(
            x[i],
            max(float(row["mae_deg"]), float(row["p90_deg"])) + 0.05,
            f"n={int(row['n'])}",
            ha="center",
            va="bottom",
            fontsize=8,
        )
    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.set_ylabel("Angular error [deg]")
    ax.set_title("SparseGaze mean and P90 error by GT event")
    ax.grid(axis="y", alpha=0.25)
    ax.legend(frameon=False)
    fig.tight_layout()
    return fig


def build_summary(
    args: argparse.Namespace,
    frame: pd.DataFrame,
    window: pd.DataFrame,
    analyzed: pd.DataFrame,
    contrast: dict[str, float],
) -> dict[str, Any]:
    return {
        "prediction_npz": str(args.prediction_npz),
        "reports_dir": str(args.reports_dir),
        "sequence": str(frame["sequence"].iloc[0]),
        "target_hz": int(frame["target_hz"].iloc[0]),
        "phase": int(frame["phase"].iloc[0]),
        "start_frame": int(window["frame_index"].min()),
        "end_frame_exclusive": int(window["frame_index"].max()) + 1,
        "window_frames": int(len(window)),
        "analyzed_frames": int(len(analyzed)),
        "include_anchors": bool(args.include_anchors),
        "event_contrast": contrast,
    }


def default_run_name(prediction_npz: Path, start_frame: int, end_frame: int | None) -> str:
    hz_phase = prediction_npz.stem
    model = prediction_npz.parents[3].name if len(prediction_npz.parents) >= 4 else "prediction"
    end_label = "end" if end_frame is None else str(end_frame)
    return f"{model}_{hz_phase}_events_{start_frame}_{end_label}"


def write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        json.dump(payload, handle, indent=2)
        handle.write("\n")


if __name__ == "__main__":
    main()
